#!/usr/bin/env node
/* ============================================================
   guard-generated.mjs — PreToolUse guard for generated files.

   Fires before Write/Edit. Denies edits to build artifacts that
   are regenerated from source, pointing the editor at the real
   source instead. Prevents hand-edits that the next build silently
   overwrites.
   ============================================================ */
import { readFileSync } from 'node:fs';
import { resolve, relative, sep } from 'node:path';

const readStdin = () => { try { return readFileSync(0, 'utf8'); } catch { return ''; } };

let data = {};
try { data = JSON.parse(readStdin() || '{}'); } catch { process.exit(0); }

const filePath = data?.tool_input?.file_path;
if (!filePath) process.exit(0);

const projectDir = process.env.CLAUDE_PROJECT_DIR || data.cwd || process.cwd();
const rel = relative(projectDir, resolve(filePath)).split(sep).join('/');

const GENERATED = {
  'partials/nav.html': 'partials/nav.html is generated by scripts/build-ia.mjs from manifest.json. Edit manifest.json (the `nav` tree) and run `node scripts/build-ia.mjs` instead.',
  'dist/wire.bundle.css': 'dist/wire.bundle.css is generated by `npm run bundle`. Edit the source CSS under css/ and re-run the bundle instead.',
};

const reason = GENERATED[rel];
if (reason) {
  process.stdout.write(JSON.stringify({
    hookSpecificOutput: {
      hookEventName: 'PreToolUse',
      permissionDecision: 'deny',
      permissionDecisionReason: reason,
    },
  }));
}
process.exit(0);
